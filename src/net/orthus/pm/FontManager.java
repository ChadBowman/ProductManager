package net.orthus.pm;

import java.awt.Font;

public class FontManager {

	
	public static Font getStatisticFont(){ return new Font("slider", Font.BOLD, 20); }
	
	public static Font getTabbedPaneFont(){ return new Font("slider", Font.PLAIN, 15); }
	
	public static Font getStandardFont(){ return new Font("slider", Font.PLAIN, 12); }

	public static Font getMonospacedFont(){ return new Font("Consolas", Font.PLAIN, 12); }
}


